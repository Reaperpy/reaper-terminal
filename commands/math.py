def add(num1, num2):
    result = num1 + num2
    print(result)


def subtract(num1, num2):
    result = num1 - num2
    print(result)


def multiply(num1, num2):
    result = num1 * num2
    print(result)


def divide_float(num1, num2):
    result = num1 / num2
    print(result)


def divide_int(num1, num2):
    result = num1 // num2
    print(result)

